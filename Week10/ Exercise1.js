// Exercise 1: Create variables and display them with document.write()

// Define variables
var num = 10;
var floatNum = 10.5;
var text = "Hello, JavaScript!";
var isTrue = true;

// Display variables using document.write()
document.write("Number: " + num + "<br>");
document.write("Float: " + floatNum + "<br>");
document.write("String: " + text + "<br>");
document.write("Boolean: " + isTrue + "<br>");
